    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked

        int aux = 0;
        Veterinario vet = new Veterinario();
        TelaLogin screenLogin = new TelaLogin();
        VeterinarioDAO vetDao = new VeterinarioDAO();

        vet.setCrmv(Integer.parseInt(TextCrmv.getText()));
        vet.setSenha(TextSenha.getText());

        for (Veterinario vet2 : vetDao.salvarVeterinarioEmLista()) {

            if (vet2.getCrmv() == Integer.parseInt(TextCrmv.getText())) {
                aux++;
            }

        }
        if (aux == 0) {
            vet.inserirVeterinarios(vet);
        } else {
            JOptionPane.showMessageDialog(null, "VETERINARIO J√Å CADASTRADO!");
        }

        screenLogin.setVisible(true);
        this.dispose();


    }//GEN-LAST:event_jLabel1MouseClicked

