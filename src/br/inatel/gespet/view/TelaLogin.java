    private void ButtonLogarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonLogarMouseClicked

        //Declaração de Objetos
        TelaPrincipal t = new TelaPrincipal();
        Veterinario vet = new Veterinario();

        //Declaração de Variaveis
        String senha;
        int crmvAux;

        //Atribuição de Valores
        try {
            crmvAux = Integer.parseInt(TextUsuario1.getText());
            senha = jPasswordField1.getText();

            //Função login retorna o crmv se ele existir no Banco caso não ele ira retonar 0
            crmv = login(crmvAux, senha);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "APENAS NUMEROS NO CRMV!");
        }

        //IF para
        if (crmv != 0) {
            t.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "VETERINÁRIO NÃO CADASTRADO");
        }
    }//GEN-LAST:event_ButtonLogarMouseClicked

