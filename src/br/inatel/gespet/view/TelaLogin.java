    //Função Login
    private int login(int crmvL, String senhaL) {

        //Declaração de Objetos
        VeterinarioDAO Vdao = new VeterinarioDAO();

        //Declaração de variaveis
        int aux = 0; //Variavel auxiliar para verificar se o CRMV esta contido no Banco

        //Foreach para varrer lista resultante do salvarVeterinarioEmLista()
        for (Veterinario veterinario : Vdao.salvarVeterinarioEmLista()) {

            //if para mostrar os dados correspondentes ao CRMV passado
            if (veterinario.getCrmv() == crmvL && veterinario.getSenha().contentEquals(senhaL)) {
                aux++;
            }
        }
        if (aux == 0) {
            return 0;
        } else {
            return crmvL;
        }
    }
